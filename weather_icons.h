enum WeatherDescriptionIndex {
    WEATHER_DESC_SUNNY,
    WEATHER_DESC_PARTLY_CLOUDY,
    WEATHER_DESC_CLOUDY,
    WEATHER_DESC_FOG,
    WEATHER_DESC_DRIZZLE,
    WEATHER_DESC_RAIN,
    WEATHER_DESC_SNOW,
    WEATHER_DESC_THUNDERSTORM,
    WEATHER_DESC_UNKNOWN
};

uint8_t weatherCodeToDescriptionIndex(uint8_t code) {
    if (code == 0) return WEATHER_DESC_SUNNY;
    if (code == 1 || code == 2) return WEATHER_DESC_PARTLY_CLOUDY;
    if (code == 3) return WEATHER_DESC_CLOUDY;
    if (code == 45 || code == 48) return WEATHER_DESC_FOG;
    if ((code >= 51 && code <= 57) || code == 66 || code == 67) return WEATHER_DESC_DRIZZLE;
    if ((code >= 61 && code <= 65) || (code >= 80 && code <= 82)) return WEATHER_DESC_RAIN;
    if ((code >= 71 && code <= 77) || code == 85 || code == 86) return WEATHER_DESC_SNOW;
    if (code >= 95 && code <= 99) return WEATHER_DESC_THUNDERSTORM;

    return WEATHER_DESC_UNKNOWN;
}

/** Convert open-meteo weather code to a human readable description */
const char* weatherCodeToDescription(uint8_t code) {
    uint8_t index = weatherCodeToDescriptionIndex(code);
    return weatherDescriptions[index];
}

